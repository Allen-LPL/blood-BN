def create_table(cursor: pymysql.cursors.Cursor, table_name: str) -> None:
	sql = f"""
	CREATE TABLE IF NOT EXISTS `{table_name}` (
	  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主键',
	  `district` VARCHAR(32) NOT NULL COMMENT '所属区',
	  `collection_site_name` VARCHAR(128) NOT NULL COMMENT '采血点名称',
	  `operating_org` VARCHAR(128) NOT NULL COMMENT '运行机构',
	  `created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
	  `updated_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
	  PRIMARY KEY (`id`),
	  UNIQUE KEY `uk_district_site` (`district`, `collection_site_name`)
	) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='北京采血点信息';
	"""
	cursor.execute(sql)


def insert_data(cursor: pymysql.cursors.Cursor, table_name: str, rows: List[Dict[str, str]]) -> int:
	insert_sql = f"""
	INSERT INTO `{table_name}` (`district`, `collection_site_name`, `operating_org`)
	VALUES (%s, %s, %s)
	ON DUPLICATE KEY UPDATE
	  `operating_org` = VALUES(`operating_org`),
	  `updated_at` = CURRENT_TIMESTAMP
	"""
	params = [(item["所属区"].strip(), item["采血点名称"].strip(), item["运行机构"].strip()) for item in rows]
	cursor.executemany(insert_sql, params)
	return len(params)


